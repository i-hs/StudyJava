package chap02;

public class SumDiff {

	public static void main(String[] args) {
		
		
		int x;
		int y;
		
		System.out.println(82 + 17);
		System.out.println(82 - 17);
		
		x = (int)83.3;
		y = (int)18.6;
		System.out.println(x+"+"+y +" = "+(x+y));
		System.out.println(x+"-"+y +" = "+(x-y));
		
	}

}
